class Book:
    def _init_(self, id, title, serie, author, desc, lang, ISBN, genres, npag, nawards, avg_vote, price):
        self.id = id
        self.title = title
        self.serie = serie
        self.autore = author
        self.desc = desc
        self.lang = lang
        self.ISBN = ISBN
        self.genres = genres
        self.npag = npag
        self.nawards = nawards
        self.avg_vote = avg_vote
        self.prezzo = price